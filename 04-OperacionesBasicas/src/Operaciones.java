public class Operaciones {
	/**
	 * 
	 * @param n1 en formato entero
	 * @param n2 en formato entero
	 * @return regresa el resultado de la suma de dos numeros
	 */
	public int sumar(int n1, int n2){
		return n1+n2;
	}
	/**
	 * 
	 * @param n1 en formato entero
	 * @param n2 en formato entero
	 * @return regresa el resultado de la resta de dos numeros
	 */
	public int restar(int n1, int n2){
		return n1-n2;
	}
	/**
	 * 
	 * @param n1 en formato entero
	 * @param n2 en formato entero
	 * @return regresa el resultado de multiplicar dos numeros
	 */
	public int multiplicar(int n1, int n2){
		return n1*n2;
	}
	/**
	 * 
	 * @param n1 en formato entero
	 * @param n2 en formato entero
	 * @return regresa el resultado de dividir dos numeros
	 */
	public int dividir(int n1, int n2){
		return n1/n2;
	}
}
